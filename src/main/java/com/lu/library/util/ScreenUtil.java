    /**
     * 设置透明状态栏(api大于19方可使用)
     * <p>可在Activity的onCreat()中调用</p>
     * <p>需在顶部控件布局中加入以下属性让内容出现在状态栏之下</p>
     * <p>android:clipToPadding="true"</p>
     * <p>android:fitsSystemWindows="true"</p>
     *
     * @param activity activity
     */
    public static void setTransparentStatusBar(Activity activity) {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.KITKAT) {
            //透明状态栏
            activity.getWindow().addFlags(WindowManager.LayoutParams.FLAG_TRANSLUCENT_STATUS);
            //透明导航栏
            activity.getWindow().addFlags(WindowManager.LayoutParams.FLAG_TRANSLUCENT_NAVIGATION);
        }
    }

    /**
     * 隐藏状态栏
     * <p>也就是设置全屏，一定要在setContentView之前调用，否则报错</p>
     * <p>此方法Activity可以继承AppCompatActivity</p>
     * <p>启动的时候状态栏会显示一下再隐藏，比如QQ的欢迎界面</p>
     * <p>在配置文件中Activity加属性android:theme="@android:style/Theme.NoTitleBar.Fullscreen"</p>
     * <p>如加了以上配置Activity不能继承AppCompatActivity，会报错</p>
     *
     * @param activity activity
     */
    public static void hideStatusBar(Activity activity) {
        activity.requestWindowFeature(Window.FEATURE_NO_TITLE);
        activity.getWindow().setFlags(WindowManager.LayoutParams.FLAG_FULLSCREEN,
                WindowManager.LayoutParams.FLAG_FULLSCREEN);
    }
    public static void addStatusHeight(final Activity activity,int res,final boolean isAddPaddingTop){
        final View viewGroup=activity.findViewById(res);
//        viewGroup.setFitsSystemWindows(true);
//        viewGroup.setClipToPadding(true);
        viewGroup.post(new Runnable() {
            @Override
            public void run() {
                ViewGroup.LayoutParams layoutParams = viewGroup.getLayoutParams();
                layoutParams.height = (ScreenUtil.getStatusBarHeight(activity) + viewGroup.getHeight());
                if (isAddPaddingTop) {
                    int paddingLeft = viewGroup.getPaddingLeft();
                    int paddingBottom = viewGroup.getPaddingBottom();
                    int paddingTop = viewGroup.getPaddingTop() + ScreenUtil.getStatusBarHeight(activity);
                    int paddingRight = viewGroup.getPaddingRight();
                    viewGroup.setPadding(paddingLeft, paddingTop, paddingRight, paddingBottom);
                }
                viewGroup.setLayoutParams(layoutParams);
            }
        });

    }
